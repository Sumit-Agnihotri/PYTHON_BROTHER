# Variable :- A variable is a name that holds up a certain value(integers, floats, strings, boolean, etc) in the memory.

# Strings
first_name="Sumit"
print(first_name)
print(f"Hello {first_name}")

# Integers
age=21
print(f"I am {age} years old")
students=100
print(f"In my class there are {students} students.")

# Float
price=10.99
print("The price of thi watch is {price}$")
distance=5.5
print(f"I ran {distance} Kilometers today.")

